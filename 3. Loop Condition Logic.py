#Task 1: Loop Condition Exploration

count = 0  # Initialize a counter

# Infinite loop
while True:
    # Print a statement
    print("Inside the infinite loop.")
    
    # Increment the counter
    count += 1
    
    # Check if the counter reaches 5
    if count == 5:
        # Break out of the loop
        break

